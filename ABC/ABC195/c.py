n = input()

l = len(n)
if l < 4:
    print(0)
elif l < 7:
    print(int(n) - 999)
elif l < 10:
    print(2*(int(n)-999999) + 999000)
elif l < 13:
    print(3*(int(n)-999999999) + 2*999000000 + 999000)
elif l < 16:
    print(4*(int(n)-999999999999) + 3*999000000000 + 2*999000000 + 999000)
else:
    print(5 + 4*999000000000000 + 3*999000000000 + 2*999000000 + 999000)